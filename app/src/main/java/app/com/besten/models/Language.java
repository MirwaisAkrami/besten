package app.com.besten.models;

public class Language {
    private String name;
    private Integer rank;
    private String content;
    private String icon;

    Language() { }

    public String getName() {
        return name;
    }

    public Integer getRank() {
        return rank;
    }

    public String getContent() {
        return content;
    }

    public String getIcon() {
        return icon;
    }
}
